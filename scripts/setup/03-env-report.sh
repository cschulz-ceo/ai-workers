## Service Dependency Build Order

Below is the recommended order for bringing up services, based on current state:

1. **NVIDIA driver** — install/update if not at 570+ (requires reboot)
2. **Docker daemon** — ensure running, NVIDIA Container Toolkit configured
3. **Ollama** — confirm running on :11434 with systemd service
4. **n8n** — start via \`docker compose up -d\` in ~/n8n/ ; update WEBHOOK_URL
5. **Open WebUI** — fix port mapping, start in ~/open-webui/
6. **ComfyUI** — start natively from ~/ComfyUI/ with GPU support
7. **Cloudflared tunnel** — expose n8n webhooks for Slack integration
8. **Slack App** — configure webhooks and slash commands
9. **Monitoring stack** — Portainer, Netdata, Uptime Kuma
10. **Plane** — bring up Docker stack in ~/plane-data/

