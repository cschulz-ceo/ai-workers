## Raw Report Files

- Preflight: \`/tmp/preflight-report.json\`
- Software:  \`/tmp/software-audit.json\`
- Ports:     \`/tmp/port-audit.json\`

EOF

# ── Terminal Summary ───────────────────────────────────────────────────────────
echo -e "${BOLD}${CYAN}═══════════════════════════════════════════${RESET}"
echo -e "${BOLD}          FINAL SUMMARY${RESET}"
echo -e "${BOLD}${CYAN}═══════════════════════════════════════════${RESET}"
echo ""

if [[ "$CRITICAL_COUNT" -eq 0 ]]; then
    echo -e "  ${GREEN}${BOLD}No critical issues found.${RESET}"
else
    echo -e "  ${RED}${BOLD}${CRITICAL_COUNT} critical issue(s) — must fix before build:${RESET}"
    echo "$ALL_CRITICAL" | grep -v "^$" | while read -r issue; do
        echo -e "    ${RED}✗${RESET} ${issue}"
    done
fi

echo ""

if [[ "$WARNING_COUNT" -eq 0 ]]; then
    echo -e "  ${GREEN}No warnings.${RESET}"
else
    echo -e "  ${YELLOW}${BOLD}${WARNING_COUNT} warning(s):${RESET}"
    echo "$ALL_WARNINGS" | grep -v "^$" | while read -r w; do
        echo -e "    ${YELLOW}△${RESET} ${w}"
    done
fi

echo ""
echo -e "  ${CYAN}${BOLD}Full report:${RESET} ${MD_REPORT}"
echo ""
